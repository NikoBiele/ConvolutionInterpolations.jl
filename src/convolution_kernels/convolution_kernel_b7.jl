function (::ConvolutionKernel{:b7,DO})(s::T) where {T,DO} # 7th order accurate 7th degree
    s_abs = abs(s)
    if DO == -1
        if s_abs >= 6
            return T(1//2) * T(sign(s))
        elseif s_abs < 1
            return horner(s_abs, b7_coefs_i1, :eq1, T, 0) * T(sign(s))
        elseif s_abs < 2
            return horner(s_abs, b7_coefs_i1, :eq2, T, 0) * T(sign(s))
        elseif s_abs < 3
            return horner(s_abs, b7_coefs_i1, :eq3, T, 0) * T(sign(s))
        elseif s_abs < 4
            return horner(s_abs, b7_coefs_i1, :eq4, T, 0) * T(sign(s))
        elseif s_abs < 5
            return horner(s_abs, b7_coefs_i1, :eq5, T, 0) * T(sign(s))
        else
            return horner(s_abs, b7_coefs_i1, :eq6, T, 0) * T(sign(s))
        end
    end
    b7_coefs_in = if DO == 0
        b7_coefs
    elseif DO == 1
        b7_coefs_d1
    elseif DO == 2
        b7_coefs_d2
    elseif DO == 3
        b7_coefs_d3
    elseif DO == 4
        b7_coefs_d4
    else
        error("kernel :b7 supports differentiation orders 0, 1, 2, 3, 4, but got $DO")
    end
    if s_abs < 1.0
        return horner(s_abs, b7_coefs_in, :eq1, T, DO) * (isodd(DO) ? T(sign(s)) : one(T))
    elseif s_abs < 2.0
        return horner(s_abs, b7_coefs_in, :eq2, T, DO) * (isodd(DO) ? T(sign(s)) : one(T))
    elseif s_abs < 3.0
        return horner(s_abs, b7_coefs_in, :eq3, T, DO) * (isodd(DO) ? T(sign(s)) : one(T))
    elseif s_abs < 4.0
        return horner(s_abs, b7_coefs_in, :eq4, T, DO) * (isodd(DO) ? T(sign(s)) : one(T))
    elseif s_abs < 5.0
        return horner(s_abs, b7_coefs_in, :eq5, T, DO) * (isodd(DO) ? T(sign(s)) : one(T))
    elseif s_abs < 6.0
        return horner(s_abs, b7_coefs_in, :eq6, T, DO) * (isodd(DO) ? T(sign(s)) : one(T))
    else
        return zero(T)
    end
