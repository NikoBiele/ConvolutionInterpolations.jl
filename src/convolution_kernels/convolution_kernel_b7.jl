function (::ConvolutionKernel{:b7})(s::T) where {T} # 7th order accurate 7th degree
    s_abs = abs(s)
    if s_abs < 1.0
        return horner(s_abs, b7_coef, :eq1, T)
    elseif s_abs < 2.0
        return horner(s_abs, b7_coef, :eq2, T)
    elseif s_abs < 3.0
        return horner(s_abs, b7_coef, :eq3, T)
    elseif s_abs < 4.0
        return horner(s_abs, b7_coef, :eq4, T)
    elseif s_abs < 5.0
        return horner(s_abs, b7_coef, :eq5, T)
    elseif s_abs < 6.0
        return horner(s_abs, b7_coef, :eq6, T)
    else
        return zero(T)
    end
end